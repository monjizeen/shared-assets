// Sorted alphabetically by name for display
const byName = [...COUNTRY_CODES].sort((a, b) => a.name.localeCompare(b.name))

/** Featured 4 (Egypt, Saudi Arabia, Turkey, Yemen) in alphabetical order, then rest by name */
export function getCountryList() {
  const featured = FEATURED_CODES.map((c) => COUNTRY_CODES.find((x) => x.code === c)).filter(Boolean)
  const rest = byName.filter((x) => !FEATURED_CODES.includes(x.code))
  return [...featured, ...rest]
}

/** Filter by code (digits) or name (case-insensitive) */
export function filterCountries(query) {
  const q = String(query ?? '').trim().toLowerCase()
  if (!q) return getCountryList()
  const list = getCountryList()
  return list.filter(
    (c) => c.code.includes(q) || c.name.toLowerCase().includes(q)
  )
}

/** Find by code or return null */
export function findCountryByCode(code) {
  return COUNTRY_CODES.find((c) => c.code === code) ?? null
}

/** All codes for parsing a full number (longest first to avoid 1 matching before 966) */
export function getCodesForParsing() {
  return [...new Set(COUNTRY_CODES.map((c) => c.code))].sort((a, b) => b.length - a.length)
}
